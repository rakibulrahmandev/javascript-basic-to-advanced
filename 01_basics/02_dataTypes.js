//? Javascript data types ------------------------------------------------------------------->

// Six type of data in javascript like 'number', 'string', 'boolean', 'null', 'undefined', 'bigint', and 'symbol';

// let's see how looks 'number' data types:
const num = 10;

// let's see how looks 'string' data types:
const str = "Rakibul Rahman";

// let's see how looks 'boolean' data types:
const boolean = true;

// let's see how looks 'null' data types:
const notAvailable = null;

// let's see how looks 'undefined' data types:
const noValue = undefined;

// And other two data types are not use regularly. most of the case we use this 'bigint' and 'symbol' data types;

// let's check the data types: (here we use the 'typeof' keywords to see what is the type in this data. this is javascript build in keywords.);
console.log(typeof num); // output: data type of 'number';
console.log(typeof str); // output: data type of 'string';
console.log(typeof boolean); // output: data type of 'boolean';
console.log(typeof notAvailable); // output: data type of 'object'; (when we see the 'null' data types, we see 'null' is an 'object'. because this is bugs on javascript.)
console.log(typeof noValue); // output: data type of 'undefined';

